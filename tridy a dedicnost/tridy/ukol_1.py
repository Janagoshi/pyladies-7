# ÚKOL: Teď, když už umíš dělat koťátka, zkus vytvořit třídu pro kočku.
#
# 1. Kočka umí mňoukat metodou zamnoukej.
# 2. Kočka má na začátku (při vytvoření) 9 životů (nemůže mít nikdy víc než 9 nebo míň než 0!).
# 3. Kočka umí říct, jestli je živá (nemá 0 životů), metodou je_ziva.
# 4. Kočka může ztratit život metodou uber_zivot.
# 5. Kočku můžeš nakrmit metodou snez, která bere 1 argument - nějaké konkrétní jídlo (řetězec). Pokud je toto jídlo "ryba", kočce se obnoví jeden život (pokud teda už není mrtvá, nebo nemá maximální počet životů).